public class OperatorAritmatika {
    public static void main(String[] args) {
        // deklarasi variabel x dan y dengan tipe int
        int x = 10;
        int y = 20;

        System.out.printf("x = %d\ny = %d\n", x, y);

        // menghitung dan menampilkan hasil operasi menggunakan operator aritmatika
        System.out.println("x + y = "+(x+y));
        System.out.println("x - y = "+(x-y));
        System.out.println("x * y = "+(x*y));
        System.out.println("x / y = "+(x/y));
    }
}
