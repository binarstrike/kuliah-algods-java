public class OperatorPembanding {
    public static void main(String[] args) {
        int x = 100;
        int y = 200;

        // menampilkan hasil perbandingan kedua variabel x dan y dengan semua jenis operator pembanding
        System.out.println("x == y: "+(x == y)); // Sama dengan
        System.out.println("x != y: "+(x != y)); // Tidak sama dengan
        System.out.println("x > y: "+(x > y)); // Lebih dari
        System.out.println("x < y: "+(x < y)); // Kurang dari
        System.out.println("x >= y: "+(x >= y)); // Lebih dari sama dengan
        System.out.println("x <= y: "+(x <= y)); // Kurang dari sama dengan
    }
}
