public class ArraySingkat {
    public static void main(String[] args) {
        int[] nilai = new int[2];
        String[] nama = new String[2];

        nilai[0] = 78;
        nilai[1] = 99;
        nama[0] = "Anton";
        nama[1] = "Yusuf";

        System.out.println(nama[0]);
        System.out.println(nama[1]);
        System.out.println(nilai[0]);
        System.out.println(nilai[1]);

        String[] siswa = { "Budi", "Binar", "Ariel", "Ani" };
        System.out.println("====================");
        System.out.println(siswa[0]);
        System.out.println(siswa[1]);
        System.out.println(siswa[2]);
        System.out.println(siswa[3]);
    }
}
